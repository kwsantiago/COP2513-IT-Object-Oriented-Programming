public class Scientific extends FourFunction{

   public void sin(){
      memory = Math.sin(memory);
   }// end sin
   
   public void cos(){
      memory = Math.cos(memory);
   }// end cos
   
   public void tan(){
      memory = Math.tan(memory);
   }// end tan
   
   public void square(){
      memory = Math.pow(memory,2);
   }// end square
   
   public void squareRoot(){
      memory = Math.sqrt(memory);
   }// end squareRoot

}// end of class